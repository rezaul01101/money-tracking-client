export default function LeftSidebar({ isSidebarOpen, setIsSidebarOpen }) {
  const pathname = usePathname();

  const handleNavigation = () => {
    // Close sidebar on mobile/tablet when clicking a navigation link
    if (window.innerWidth < 768) {
      setIsSidebarOpen(false);
    }
  };

  return (
    <div>
      <div
        className={`fixed inset-y-0 left-0 z-30 w-64 bg-white shadow-lg transform ${
          isSidebarOpen ? "translate-x-0" : "-translate-x-full"
        } transition-transform duration-300 ease-in-out`}
      >
        {/* Logo */}
        <div className="flex items-center h-16 px-4 border-b">
          <div className="flex items-center">
            <div className="w-8 h-8 bg-black rounded-lg flex items-center justify-center">
              <span className="text-white font-bold">E</span>
            </div>
            <span className="ml-2 text-xl font-semibold">Expense App</span>
          </div>
        </div>

        {/* Navigation */}
        <nav className="px-4 mt-6">
          {navigation.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.name}
                href={item.href}
                onClick={handleNavigation}
                className={`flex items-center px-2 py-2 mt-2 text-sm font-medium rounded-lg ${
                  isActive
                    ? "text-pink-700 bg-pink-50"
                    : "text-gray-600 hover:text-pink-700 hover:bg-pink-50"
                }`}
              >
                <svg
                  className={`w-5 h-5 mr-3 ${
                    isActive ? "text-pink-700" : "text-gray-400"
                  }`}
                  fill="none"
                  stroke="currentColor"
                  viewBox="0 0 24 24"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d={item.icon}
                  />
                </svg>
                {item.name}
              </Link>
            );
          })}
        </nav>
      </div>

      {/* Backdrop for mobile */}
      {isSidebarOpen && (
        <div
          className="fixed inset-0 bg-opacity-50 z-20 md:hidden"
          onClick={() => setIsSidebarOpen(false)}
        ></div>
      )}
    </div>
  );
}
